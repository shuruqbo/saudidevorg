#use four methods on a list:
list1 = ["flashlight", 'torch', "lamp", "lantern"]
print("Original list: ")
print(list1)

# 1.adding an item
list1.append("sunlight")
print("Adding an item")
print(list1)
print("--")

# 2.sorting items in the list
list1.sort()
print("after sorting the items out")
print(list1)
print("--")
# 3. using pop() to remove the last item
list1.pop()
print("using pop() with no index will delete the last item")
print(list1)
print("--")
# 4. using the reverse() method 
print(list1)
print("after the reverse")
list1.reverse()
print(list1)

